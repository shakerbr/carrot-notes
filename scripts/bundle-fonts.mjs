import { cp, mkdir, writeFile, rm } from 'node:fs/promises';
import { join } from 'node:path';

const outDir = 'src/fonts';
const cssPath = 'src/css/fonts.css';

const bundles = [
  {
    family: 'Inter',
    pkgDir: 'node_modules/@fontsource/inter/files',
    files: [
      { file: 'inter-latin-400-normal.woff2', weight: 400 },
      { file: 'inter-latin-500-normal.woff2', weight: 500 },
      { file: 'inter-latin-600-normal.woff2', weight: 600 },
      { file: 'inter-latin-700-normal.woff2', weight: 700 },
    ],
  },
  {
    family: 'Caveat',
    pkgDir: 'node_modules/@fontsource/caveat/files',
    files: [
      { file: 'caveat-latin-400-normal.woff2', weight: 400 },
      { file: 'caveat-latin-500-normal.woff2', weight: 500 },
      { file: 'caveat-latin-600-normal.woff2', weight: 600 },
      { file: 'caveat-latin-700-normal.woff2', weight: 700 },
    ],
  },
];

await rm(outDir, { recursive: true, force: true });
await mkdir(outDir, { recursive: true });

const rules = [];

for (const bundle of bundles) {
  for (const entry of bundle.files) {
    const src = join(bundle.pkgDir, entry.file);
    const dest = join(outDir, entry.file);
    await cp(src, dest);
    rules.push(`@font-face {
  font-family: '${bundle.family}';
  font-style: normal;
  font-display: swap;
  font-weight: ${entry.weight};
  src: url('../fonts/${entry.file}') format('woff2');
}`);
  }
}

const css = `/* Generated by scripts/bundle-fonts.mjs — do not edit manually */
${rules.join('\n\n')}
`;

await writeFile(cssPath, css);
console.log(`Bundled Inter and Caveat into ${cssPath}`);
